package com.amx.jax.dbmodel.promotion;

import java.math.BigDecimal;

public class PromotionLocationModel {

	BigDecimal comCode;

	BigDecimal documentNo;

	BigDecimal docFinYear;

	BigDecimal locCode;

	public BigDecimal getComCode() {
		return comCode;
	}

	public void setComCode(BigDecimal comCode) {
		this.comCode = comCode;
	}

	public BigDecimal getDocumentNo() {
		return documentNo;
	}

	public void setDocumentNo(BigDecimal documentNo) {
		this.documentNo = documentNo;
	}

	public BigDecimal getDocFinYear() {
		return docFinYear;
	}

	public void setDocFinYear(BigDecimal docFinYear) {
		this.docFinYear = docFinYear;
	}

	public BigDecimal getLocCode() {
		return locCode;
	}

	public void setLocCode(BigDecimal locCode) {
		this.locCode = locCode;
	}

}
